            <h3>The CARMA Center</h3>
           <p>Atrial fibrillation is the most common heart arrhythmia, affecting
            millions of people worldwide and is associated with a heightened
            risk of stroke and an overall increase in morbidity and
            mortality. The CARMA Center at the University of Utah is advancing
            research to understand this disease and to improve medical
            techniques and interventions that greatly improve the lives of
            patients
            
          <p>In support of CARMA's mission to redefine the diagnosis and management
            of atrial fibrillation (AF), the Slicer CMR Toolkit software incorporates
            image processing algorithms and MRI volume visualization
            to produce detailed patient-specific models of heart structures, quantify
            LA fibrosis that is associated with AF progression, and assess the
            regional patterning and extent of post-radiofrequency-ablation scarring.
            Slicer CMR Toolkit also serves as a platform for rapid prototyping and deployment of
            new cardiac image analysis algorithms.  At CARMA, the unique embedding of
            dedicated software architects and image processing scientists within the
            hospital environment has produced a streamlined protocol for clinical work
            that has been designed, in part, by the clinicians themselves.
            
            <h3>Fibrosis and Scar Quantification Workflow</h3> 
          <P> The Slicer CMR Toolkit was
            developed primarily to support a clinical workflow developed at CARMA and the
            University of Utah for (1) segmentation of cardiac structures from MRI, (2)
            quantification of the amount of fibrosis and scar in the myocardium of
            cardiac chambers, and (3) visualization of 3D models of cardiac chambers and
            tissue damage.
            
            
