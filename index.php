  <h4>The CARMA Center and the Slicer CMR Toolkit</h4>

  <p> The Cardiac MRI (CMR) Toolkit Slicer extension is a collection of
  modules that can be used in the analysis of cardiac MRI images.  The
  primary purpose of this toolkit is the analysis of LGE-MRI images
  for the study and treatment of cardiac arrhythmia at the <a
   href="http://www.na-mic.org/pages/DBP:Atrial_Fibrillation">NAMIC A
  trial Fibrillation DBP.  These modules were developed at the <a
   href="http://healthsciences.utah.edu/carma/">Comprehensive
  Arrhythmia Research and Management (CARMA) Center</a>.

  The CMR
  Toolkit implements cardiac MRI postprocessing workflows for the
  study and treatment of atrial fibrillation.  Atrial fibrillation is
  the most common heart arrhythmia, affecting millions of people
  worldwide and is associated with a heightened risk of stroke and an
  overall increase in morbidity and mortality. The CARMA Center at the
  University of Utah is advancing research to understand this disease
  and to improve medical techniques and interventions that greatly
  improve the lives of patients
  
  <p>In support of CARMA's mission to redefine the diagnosis and management
  of atrial fibrillation (AF), the Slicer CMR Toolkit software incorporates
  state-of-the-art image processing algorithms and MRI volume visualization
  to produce detailed patient-specific models of heart structures, quantify
  LA fibrosis that is associated with AF progression, and assess the
  regional patterning and extent of post-radiofrequency-ablation scarring.
  Slicer CMR Toolkit also serves as a platform for rapid prototyping and deployment of
  new cardiac image analysis algorithms.  At CARMA, the unique embedding of
  dedicated software architects and image processing scientists within the
  hospital environment has produced a streamlined protocol for clinical work
  that has been designed, in part, by the clinicians themselves.

